// 变量默认值
package main

import "fmt"

func main()  {
	// 声明变量
	var i int32
	var f float32
	var b bool
	var c complex64
	var s string

	fmt.Print("int32 default:")
	fmt.Println(i)
	fmt.Print("float default:")
	fmt.Println(f)
	fmt.Print("bool default:")
	fmt.Println(b)
	fmt.Print("complex default:")
	fmt.Println(c)
	fmt.Print("string default:")
	fmt.Println(s)
}
